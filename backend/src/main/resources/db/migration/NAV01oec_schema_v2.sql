CREATE VIEW public.credit_distribution_view AS
 SELECT parent.id AS credit_parent_id,
    parent.reference AS parent_reference,
    art.id AS article_id,
    art.name AS article_name,
    art.marque AS brand,
    art.model,
    ca_parent.quantity AS parent_quantity,
    COALESCE(sum(ca_child.quantity), (0)::bigint) AS distributed_quantity,
    (ca_parent.quantity - COALESCE(sum(ca_child.quantity), (0)::bigint)) AS undistributed_quantity
   FROM ((((public.credit parent
     JOIN public.credit_articles ca_parent ON (((parent.id = ca_parent.credit_id) AND ((ca_parent.visibility)::text = 'ENABLED'::text))))
     JOIN public.articles art ON (((ca_parent.articles_id = art.id) AND ((art.visibility)::text = 'ENABLED'::text))))
     LEFT JOIN public.credit child ON (((parent.id = child.parent_id) AND ((child.visibility)::text = 'ENABLED'::text))))
     LEFT JOIN public.credit_articles ca_child ON (((child.id = ca_child.credit_id) AND ((ca_child.visibility)::text = 'ENABLED'::text) AND (ca_child.articles_id = art.id))))
  WHERE ((parent.parent_id IS NULL) AND ((parent.client_type)::text = 'PROMOTER'::text) AND ((parent.status)::text = 'INPROGRESS'::text) AND ((parent.visibility)::text = 'ENABLED'::text))
  GROUP BY parent.id, parent.reference, art.id, art.name, art.marque, art.model, ca_parent.quantity;


