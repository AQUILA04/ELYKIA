-- Create Cash Deposit Table
CREATE TABLE IF NOT EXISTS public.cash_deposit (
    id bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    reg_user_id character varying(50) NOT NULL,
    date_reg timestamp(6) without time zone NOT NULL,
    mod_user_id character varying(50),
    date_mod timestamp(6) without time zone,
    visibility character varying(255) NOT NULL,
    
    date date NOT NULL,
    commercial_username character varying(255) NOT NULL,
    amount double precision NOT NULL,
    billetage text,
    received_by character varying(255),
    daily_report_id bigint,
    
    CONSTRAINT cash_deposit_pkey PRIMARY KEY (id),
    CONSTRAINT cash_deposit_visibility_check CHECK (((visibility)::text = ANY ((ARRAY['ENABLED'::character varying, 'DISABLED'::character varying, 'DELETED'::character varying])::text[]))),
    CONSTRAINT fk_cash_deposit_daily_report FOREIGN KEY (daily_report_id) REFERENCES public.daily_commercial_report(id)
);
